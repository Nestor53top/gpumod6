static const int GPU_DATABASE_SIZE = sizeof(GPU_DATABASE) / sizeof(GPU_DATABASE[0]);

static QStringList getAllGpuNames() {
    QStringList names;
    for (int i = 0; i < GPU_DATABASE_SIZE; i++) {
        names << GPU_DATABASE[i].name;
    }
    return names;
}

static QStringList filterGpuNames(const QString& filter) {
    if (filter.isEmpty()) return getAllGpuNames();
    QStringList result;
    QString lower = filter.toLower();
    for (int i = 0; i < GPU_DATABASE_SIZE; i++) {
        if (GPU_DATABASE[i].name.toLower().contains(lower) ||
            GPU_DATABASE[i].brand.toLower().contains(lower)) {
            result << GPU_DATABASE[i].name;
        }
    }
    return result;
}

static int getDefaultVramMB(const QString& name) {
    for (int i = 0; i < GPU_DATABASE_SIZE; i++) {
        if (GPU_DATABASE[i].name == name) return GPU_DATABASE[i].vramDefaultMB;
    }
    return 0;
}